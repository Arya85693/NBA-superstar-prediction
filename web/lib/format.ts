/** Dollar amounts and stock-style prices: always 2 decimal places. */
export function formatUsd(n: number): string {
  return n.toLocaleString("en-US", {
    style: "currency",
    currency: "USD",
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
}

/** Same as display amount without the $ (for column alignment: $ in one cell, number next to it). */
export function formatUsdNumberOnly(n: number): string {
  return n.toLocaleString("en-US", {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  });
}
